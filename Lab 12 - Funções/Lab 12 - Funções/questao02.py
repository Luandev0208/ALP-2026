# função input() recebe uma mensagem, lê um valor digitado e retorna uma string
nome = input("Digite seu nome: ")

# função print() exibe informações na tela
print(nome)

# função int() converte um valor para inteiro
idade = int("18")

# função float() converte um valor para decimal
altura = float("1.75")

# função str() converte um valor para texto
texto = str(123)

# função bool() converte um valor para booleano
valor = bool("python")

# função len() retorna a quantidade de caracteres
tamanho = len("python")

# função type() mostra o tipo da variável
print(type(idade))
